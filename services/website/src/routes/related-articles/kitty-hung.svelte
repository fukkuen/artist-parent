<h1>和孩子觀看世界的 100 種方法</h1>
<blockquote>
	<p>The child</p>
	<p>is made of one hundred.</p>
	<p>The child has</p>
	<p>a hundred languages</p>
	<p>a hundred hands</p>
	<p>a hundred thoughts</p>
	<p>a hundred ways of thinking</p>
	<p>of playing, of speaking.</p>
	<p>— 100 languages, by Loris Malaguzzi (translated by Lella Gandini)</p>
</blockquote>

<p>瑞吉歐教學法（Reggio Emilia Approach）的創辦人 Loris Malaguzzi
	著名的詩作〈孩子的一百種語言〉中寫道，有別於碎片化的學校教育模式，孩子天生就有一百種聆聽、歡頌、驚嘆、愛慕、發現世界的方法。這半年間剛好我和孩子也在不停實驗認識世界的方法，走在路上踩一口被太陽烘得像脆餅的落葉、尋找藏匿在街道和建築物上的各種形狀、想像文字的故事、辨認星空裡閃耀的神話。</p>
<p>近年流行 STEAM，生產商製造了一套又一套 STEAM
	的教具，讓孩子們可以和家長在拼拼砌砌的過程中又學到科學又學到藝術，但說到底美學是一種生活上的經驗感知，藉由觀察發現當中的邏輯與想像空間，而孩子本來就是天生的藝術家，大人可以做的是鼓勵他們的天馬行空。</p>
<p>《事件譜之又係藝術家又係阿爸阿媽》像是打開了一扇窗，讓讀者看見藝術家們和他們的孩子觀看世界的方法，47 種事件就是 47 種生活美感
	project，大人和孩子大可以把此書當成一場美學發現的入門地圖，由一張畫紙、一片樹葉、一顆月亮裡發掘創作的元素，讓藝術創作在小孩生活的場景裡發生，或許孩子們也會從每一個小 project 裡，變幻出一百種好玩的方法來。</p>